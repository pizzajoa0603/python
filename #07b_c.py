# 계산기

n1 = int(input('수1: '))
n2 = int(input('수2:'))


s = n1+ n2
s2 = n1- n2
s3 = n1* n2
s4= n1/ n2
q = n1// n2
r = n1% n2

print(f' {n1} + {n2} = {s} ')
print(f' {n1} - {n2} = {s2} ')
print(f' {n1} * {n2} = {s3} ')
print(f' {n1} / {n2} = {s4} ')
print(f' {n1} //{n2} = {q} ')
print(f' {n1} % {n2} = {r} ')
